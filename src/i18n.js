const localeListeners = new Set();

export function getLocale() {
  const stored = localStorage.getItem(LOCALE_KEY);
  return stored && MESSAGES[stored] ? stored : DEFAULT_LOCALE;
}

export function t(key, params = {}) {
  const locale = getLocale();
  let text = MESSAGES[locale]?.[key] ?? MESSAGES[DEFAULT_LOCALE]?.[key] ?? key;
  for (const [name, value] of Object.entries(params)) {
    text = text.replace(new RegExp(`\\{${name}\\}`, 'g'), String(value));
  }
  return text;
}

export function setLocale(locale) {
  if (!MESSAGES[locale]) return;
  localStorage.setItem(LOCALE_KEY, locale);
  document.documentElement.lang = locale;
  applyI18n();
  syncLangSwitcherUi();
  localeListeners.forEach((listener) => listener(locale));
}

export function onLocaleChange(listener) {
  localeListeners.add(listener);
  return () => localeListeners.delete(listener);
}

export function applyI18n(root = document) {
  document.documentElement.lang = getLocale();
  applySeoMetaTags({
    locale: getLocale(),
    title: t('meta.title'),
    description: t('meta.description'),
    ogTitle: t('meta.ogTitle'),
    keywords: t('meta.keywords'),
    author: t('meta.author'),
  });

  root.querySelectorAll('[data-i18n]').forEach((el) => {
    if (el.dataset.i18nParams) {
      let params = {};
      try {
        params = JSON.parse(el.dataset.i18nParams);
      } catch {
        params = {};
      }
      el.textContent = t(el.dataset.i18n, params);
      return;
    }
    el.textContent = t(el.dataset.i18n);
  });

  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  root.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });

  root.querySelectorAll('[data-i18n-aria-label]').forEach((el) => {
    el.setAttribute('aria-label', t(el.dataset.i18nAriaLabel));
  });

  root.querySelectorAll('[data-i18n-html]').forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
}

export function getBereichMark(bereich) {
  const isInvoices = bereich === 'rechnungen';
  if (getLocale() === 'en') {
    return isInvoices ? 'I' : 'Q';
  }
  return isInvoices ? 'R' : 'A';
}

export function syncBereichMarks(root = document) {
  root.querySelectorAll('[data-bereich-mark]').forEach((el) => {
    el.textContent = getBereichMark(el.dataset.bereichMark);
  });
}

export function syncLangSwitcherUi() {
  const locale = getLocale();

  document.querySelectorAll('.lang-switcher').forEach((wrap) => {
    mountFlag(wrap.querySelector('.lang-switcher__flag'), locale);

    wrap.querySelectorAll('[data-locale]').forEach((btn) => {
      const active = btn.dataset.locale === locale;
      btn.classList.toggle('is-active', active);
      btn.setAttribute('aria-checked', String(active));
    });
  });

  mountAllFlags();
}

export function initI18n() {
  document.documentElement.lang = getLocale();
  mountAllFlags();
  applyI18n();
  syncLangSwitcherUi();
}
